#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t		length;
	size_t		i;
	char		*dup;

	length = ft_strlen(s);
	dup = (char *)malloc(length + 1);
	if (!dup)
		return (NULL);
	i = 0;
	while (i < length)
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
